        <br><p class="text-left entrada">Rumores en la industria de la tecnología dice que Apple lanzará una serie de sus más recientes dispositivos en uno de sus eventos de marzo. Según un reporte de Yibada, la compañía planea lazar una nueva serie de Apple Watch, el iPad Air 3 y una nueva línea del iPhone. Apple ha tenido dificultades con sus ventas ya que se reportó que solo pudieron vender cerca de 5.3 millones de productos MacBook en el último cuarto de 2015. <a href="#" class="hidden-lg hidden.md">Ver Más</a></p> <p class="text-left entrada hidden-xs"> La sorprendente caída en ventas fue de siete por ciento menos que el cuarto anterior.

        <h4 class="text-center">Adobe lanza hoy Animate CC, su antiguo creador de Flash</h4>
        <br><p class="text-left entrada">Lo anunciaron en noviembre y hoy ya es una realidad. La herramienta conocida hasta ahora por Flash Professional se llama ahora Animate CC, ofreciendo recursos que permiten no limitarse a los archivos flash y sí poder trabajar con HTML5, exportar en WebGL, AIR, video 4k, svg, .OAM y otros, aunque, según indican en TC, el 33% de lo que se creaba con Flash Professional ya era HTML5. <a href="#" class="hidden-lg hidden.md">Ver Más</a></p> <p class="text-left entrada hidden-xs"> Disponible para los suscriptores de Creative Cloud, hacen el lanzamiento al mismo tiempo que actualizan su herramienta de creación de sitios web Muse, plataforma desde la cual podemos webs sin necesidad de programar.
        Podéis ver los detalles en adobe.com, donde lo presentan como una solución que permite diseñar animaciones interactivas con herramientas de dibujo vanguardistas para publicarlas en diferentes plataformas, como Flash/AIR, HTML5 Canvas, WebGL o incluso plataformas personalizadas, para llegar a usuarios de prácticamente cualquier ordenador de sobremesa o dispositivo móvil.
        En caso de no ser suscriptores de Creative Cloud, es posible usar la herramienta por 24,19 €/mes.</p>


        "Se espera que fabricantes de la cadena de suministro comiencen a producir nuevas MacBooks de 12" y 13.3", al final del primer cuarto o a principios del segundo, y modelos de 15" en el tercer cuarto", reportó Digitimes.
        Conforme progresa la industria de la tecnología, Apple continuamente sigue en la cima pero, con mucha competencia, la compañía dio un paso adelante al lanzar nuevos productos este año.
        Uno de los dispositivos que emocionan a fans es el iPad Air 3, que posiblemente podría ser lanzado el 15 de marzo, 2016 junto con otros dispositivos de Apple, reportó MacWorld.</p>

